    private void id_pcKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_id_pcKeyPressed
        // TODO add your handling code here:
        id=id_pc.getText();
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try{
                sql="select * from db_sewapc "
                + "where id_pc='"+ id + "'";
                stat=con.createStatement();
                res=stat.executeQuery(sql);
                while (res.next()){
                    deskripsicombobox.setSelectedItem(res.getString("deskripsi_kebutuhan_pc"));
                    merkcombobox.setSelectedItem(res.getString(merk));
                    
                    statuscombobox.setSelectedItem(res.getString("status"));
                    JOptionPane.showMessageDialog(null, "Data Ditemukan");
                }
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Tidak Ditemukan \n"+e.getMessage());
                deskripsicombobox.requestFocus();
            }
        }
    }//GEN-LAST:event_id_pcKeyPressed

