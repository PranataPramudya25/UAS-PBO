    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        id=String.valueOf(id_pc.getText());
        deskripsi=String.valueOf(deskripsicombobox.getSelectedItem());
        merk=String.valueOf(merkcombobox.getSelectedItem());
       
        status=String.valueOf(statuscombobox.getSelectedItem());
        try{
            sql="INSERT INTO db_sewapc(id_pc, deskripsi_kebutuhan_pc,"
            + "merk_pc, "
            + "status)value "
            + "('"+ id +"','"+ deskripsi +"','"+ merk +"',"
            + "'"+"','"+ status +"')";
            stat=con.createStatement();
            stat.execute(sql);
            kosongkan();
            TampilData(sql);
            JOptionPane.showMessageDialog(null,
                "Data Berhasil Disimpan");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,
                "Data Gagal Disimpan\n"+e.getMessage());
        }
    }//GEN-LAST:event_SimpanActionPerformed

