private void TampilData (String sql){
    DefaultTableModel t = new DefaultTableModel();
    
    t.addColumn("ID PC");
    t.addColumn("Deskripsi Kebutuhan Pc");
    t.addColumn("Merek Pc");
   
    t.addColumn("Status");
    Table.setModel(t);
    try {
        int i = 1;
        stat=con.createStatement();
        res=stat.executeQuery("select * from db_sewapc");
        while (res.next()) {
            t.addRow(new Object[]{
            res.getString("id_pc"),
            res.getString("deskripsi_kebutuhan_pc"),
            res.getString("merk_pc"),
           
            res.getString("status"),
            });
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
    }
}



